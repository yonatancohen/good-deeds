#!/usr/bin/env node
/**
 * Reads the generated Tailwind CSS and writes it as a TypeScript module
 * so it can be bundled and injected into the web DOM by _layout.tsx.
 * Run via: npm run tw:build
 */
const fs = require('fs');
const path = require('path');

const cssPath = path.join(__dirname, '../public/tailwind.css');
const outPath = path.join(__dirname, '../lib/injectWebCSS.web.ts');

const css = fs.readFileSync(cssPath, 'utf8');
const escaped = JSON.stringify(css);

const NATIVE_FEEL_CSS = `
/* Native-feel PWA: touch, scroll, selection (appended before Tailwind) */
html, body {
  overscroll-behavior: none;
  width: 100%;
  max-width: 100%;
  overflow: hidden;
  height: 100%;
  direction: ltr;
}
/*
 * RTL on #root only — avoids iOS PWA viewport shift from dir=rtl on <html>.
 * Lock shell height so only in-app ScrollViews scroll, not the page body.
 */
html, body {
  min-height: 100%;
}
#root {
  direction: rtl;
  unicode-bidi: isolate;
  display: flex;
  flex-direction: column;
  align-items: stretch;
  width: 100%;
  max-width: 100%;
  min-width: 100%;
  height: 100dvh;
  max-height: 100dvh;
  overflow: hidden;
  flex: 1;
  margin: 0;
}
#root > div {
  direction: rtl;
  display: flex;
  flex-direction: column;
  flex: 1;
  min-height: 0;
  overflow: hidden;
  width: 100%;
  max-width: 100%;
  min-width: 100%;
}
* {
  -webkit-tap-highlight-color: transparent;
}
button, a, [role="button"], summary, label, input[type="submit"], input[type="button"] {
  touch-action: manipulation;
  -webkit-touch-callout: none;
}
input, textarea, select, [contenteditable="true"] {
  touch-action: manipulation;
  -webkit-touch-callout: default;
  user-select: text;
  -webkit-user-select: text;
}
body {
  user-select: none;
  -webkit-user-select: none;
}
#root [style*="overflow"] {
  -webkit-overflow-scrolling: touch;
}
`;

const ANIMATIONS = `
@keyframes liquidFlow {
  0%   { background-position: 200% center; }
  100% { background-position: -200% center; }
}
`;

const content = `// AUTO-GENERATED by \`npm run tw:build\` — do not edit manually.
// Contains Tailwind CSS inlined for web injection via useEffect.

let injected = false;

const ANIMATIONS = ${JSON.stringify(ANIMATIONS)};
const NATIVE_FEEL_CSS = ${JSON.stringify(NATIVE_FEEL_CSS)};

export function injectWebCSS(): void {
  if (typeof document === 'undefined' || injected) return;
  injected = true;
  const existing = document.getElementById('app-tailwind');
  if (existing) return;
  const style = document.createElement('style');
  style.id = 'app-tailwind';
  style.textContent = ANIMATIONS + NATIVE_FEEL_CSS + CSS;
  document.head.appendChild(style);
}

const CSS = ${escaped};
`;

fs.writeFileSync(outPath, content, 'utf8');
console.log(`✅ injectWebCSS.web.ts updated (${Math.round(css.length / 1024)}KB CSS inlined)`);
